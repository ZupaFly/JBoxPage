function showDropdown(index) {
  var dropdown = document.getElementById("dropdown");
  var songDropdown = document.getElementById("songDropdown");
  let searchInput = document.getElementById('searchInput');

  // Очищаем предыдущие элементы
  songDropdown.innerHTML = "";

  // Заполняем выпадающий список всеми песнями из массива
  document.addEventListener('click', function(event){
    let clickedElementFromList = event.target;
    let elementId = clickedElementFromList.id.slice(-1);

    if (!isNaN(parseFloat(elementId)) && elementId % 2 !== 0) {
      songDropdown.innerHTML = "";

      for (let i = 0; i < chinese.length; i++) {
        let optionChin = document.createElement("option");
        optionChin.value = i;
        optionChin.text = chinese[i].name + " - " + chinese[i].duration;
        songDropdown.add(optionChin);
      }
    };

    if (!isNaN(parseFloat(elementId)) && elementId % 2 === 0) {
      songDropdown.innerHTML = "";

      for (let y = 0; y < eng.length; y++) {
        let optionEng = document.createElement("option");
        optionEng.value = y;
        optionEng.text = eng[y].name + " - " + eng[y].duration;
        songDropdown.add(optionEng);
      }
    }
  });

  searchInput.addEventListener('input', function(event) {
    let searchTerm = event.target.value.toLowerCase();
    let filteredSongs = chinese.concat(eng)
      .filter(song => song.name.toLowerCase().includes(searchTerm))
      .map((song, index) => {
        let option = document.createElement("option");
        option.value = index;
        option.text = song.name + " - " + song.duration;
        return option;
      });
    songDropdown.innerHTML = "";
    filteredSongs.forEach(option => songDropdown.add(option));
  });

  // Сохраняем название выбранной песни
  selectedSongName = document.getElementById(index).textContent;

  // Показываем выпадающий список
  dropdown.style.display = "block";
  // Сохраняем индекс элемента, на который был сделан клик
  dropdown.dataset.clickedIndex = index;
};

function replaceSong() {
  let dropdown = document.getElementById("dropdown");
  let songDropdown = document.getElementById("songDropdown");
  let selectedSongIndex = songDropdown.value;
  let clickedIndex = dropdown.dataset.clickedIndex;
  let lastFiveDigitsSong = selectedSongName.slice(-5);

  // Получаем выбранную песню из выпадающего списка
  let elementId = clickedIndex.slice(-1);

  if (!isNaN(parseFloat(elementId)) && elementId % 2 !== 0) {
    selectedSong = chinese[selectedSongIndex];
  } else if (!isNaN(parseFloat(elementId)) && elementId % 2 === 0) {
    selectedSong = eng[selectedSongIndex];
  }

  // Заменяем текст песни в элементе списка
  let clickedElement = document.getElementById(clickedIndex);
  clickedElement.innerHTML = selectedSong.name + " - " + selectedSong.duration;

  let parts = clickedIndex.split('_');
  let firstDigitAfterUnderscore = parts[1];

  for (let changeSongIdIndex = 0; changeSongIdIndex <= firstDigitAfterUnderscore; changeSongIdIndex++) {
    let newSetLength = document.getElementById(`set_${changeSongIdIndex}`);

    if (newSetLength) {
      let lastFiveDigitsSet = newSetLength.textContent.slice(-5);

      let changedSetLength 
      = converterToSeconds(lastFiveDigitsSet) 
        + converterToSeconds(selectedSong.duration)
        - converterToSeconds(lastFiveDigitsSong);

      newSetLength.innerText 
        = `Total Set length:${timerGenerator(changedSetLength)}`;
    }
  }

  // Скрываем выпадающий список
  dropdown.style.display = "none";
}


